void	ft_swap(int *a, int *b)
{
	*a ^= *b;
	*b ^= *a;
	*a ^= *b;
}

#include <stdio.h>

int	main(void)
{
	int a, b;

	a = 21;
	b = a * 2;
	printf("a : %d, b : %d\n", a, b);
	ft_swap(&a, &b);
	printf("a : %d, b : %d\n", a, b);
	return (0);
}
