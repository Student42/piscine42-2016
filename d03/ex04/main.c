void	ft_ultimate_div_mod(int *a, int *b)
{
	int		c;

	c = (*a % 2) ? 1 : 0;
	*a = *a / *b;
	*b = (*a * *b) % *b + c;
}

int		main(void)
{
	int	a = 0;
	int b = 5;
	ft_ultimate_div_mod(&a, &b);
	printf("a : %d, b : %d\n", a, b);
	return (0);
}
